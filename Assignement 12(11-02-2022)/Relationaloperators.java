package project;

public class Relationaloperators {

	public static void main(String[] args) {
       int num1=10,num2=20;
       System.out.println("The number 1" + num1 );
       System.out.println("The number 2" + num2 );
       System.out.println("The result of comparison :" +(num1 == num2));
       System.out.println("The result of not equal to :" +(num1 != num2));
       System.out.println("The result of greater than :" +(num1 > num2));
       System.out.println("The result of lesser than :" +(num1 < num2));
       System.out.println("The result of greater than or equal to :" +(num1>=num2));
       System.out.println("The result of lesser than or equal to  :" +(num1 <= num2));




	}

}
